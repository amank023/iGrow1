    <script>
        $(document).ready(function () {
            $('#linkCity').css('display', 'none');
        });
        var div = document.getElementById("location");
        function getLocation() {
            if (navigator.geolocation) {
                navigator.geolocation.getCurrentPosition(showPosition, showError, setCookies);
            } else {
                div.innerHTML = "The Browser Does not Support Geolocation";
            }
        }

        function showPosition(position) {
            $.get("https://ipinfo.io", function (response) {
                if (response.country == 'IN') {
                    $('#indian').css("display", "block");
                    $('#international').css("display", "none");
                    $('#indian1').css("display", "block");
                    $('#international1').css("display", "none");
                    $('#indian2').css("display", "block");
                    $('#international2').css("display", "none");
                    $('.forindian').css("display", "block");
                    $('.forinternational').css("display", "none");
                }
                else {
                    $('#international').css("display", "block");
                    $('#indian').css("display", "none");
                    $('#international1').css("display", "block");
                    $('#indian1').css("display", "none");
                    $('#international2').css("display", "block");
                    $('#indian2').css("display", "none");
                    $('.forinternational').css("display", "block");
                    $('.forindian').css("display", "none");
                }
                //   $('#city').php('<p> '+response.country+'</p>');
            }, "jsonp");
            document.getElementById("latitude1").value = position.coords.latitude;
            document.getElementById("longitude1").value = position.coords.longitude;
            document.getElementById("userLatitude").value = position.coords.latitude;
            document.getElementById("userLangitude").value = position.coords.longitude;
        }

        function showError(error) {
            if (error.PERMISSION_DENIED) {
                $('#city').php('<button class="btn primany select location" id="myBtn12"><i style="color:red;" class="d-icon-map"></i> Permission Denied</button>');
            }
        }

        function setCookies() {
            Cookies.set('userLocation', 'Yes');
        }
        getLocation();
